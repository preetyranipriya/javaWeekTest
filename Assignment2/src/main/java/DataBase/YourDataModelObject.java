package DataBase;


public class YourDataModelObject {
    private String date;
    private String month;
    private String team;
    private String panel_Name;
    private String round;
    private String skill;
    private String time;
    private String candidate_Current_Loc;
    private String candidate_Preferred_Loc;
    private String candidate_Name;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public String getPanel_Name() {
        return panel_Name;
    }

    public void setPanel_Name(String panel_Name) {
        this.panel_Name = panel_Name;
    }

    public String getRound() {
        return round;
    }

    public void setRound(String round) {
        this.round = round;
    }

    public String getSkill() {
        return skill;
    }

    public void setSkill(String skill) {
        this.skill = skill;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getCandidate_Current_Loc() {
        return candidate_Current_Loc;
    }

    public void setCandidate_Current_Loc(String candidate_Current_Loc) {
        this.candidate_Current_Loc = candidate_Current_Loc;
    }

    public String getCandidate_Preferred_Loc() {
        return candidate_Preferred_Loc;
    }

    public void setCandidate_Preferred_Loc(String candidate_Preferred_Loc) {
        this.candidate_Preferred_Loc = candidate_Preferred_Loc;
    }

    public String getCandidate_Name() {
        return candidate_Name;
    }

    public void setCandidate_Name(String candidate_Name) {
        this.candidate_Name = candidate_Name;
    }
}
